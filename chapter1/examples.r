# R Değişken Türleri Örnekleri



# Sayısal (numeric): 
# Sayı değerlerini tutar, matematiksel işlemler için kullanılır
number <- 42


# Karakter (character): 
# Metinsel verileri tutar, isim, açıklama gibi
text <- "R Dili"


# Mantıksal (logical): 
# TRUE veya FALSE değerlerini tutar, koşul ve karşılaştırmalarda kullanılır
logical_value <- FALSE


# Vektör (vector): 
# Aynı türden birden fazla değeri tek değişkende tutar, toplu işlemler için kullanılır
vector <- c(1, 2, 3, 4)


# Liste (list): 
# Farklı türden verileri bir arada tutar, karmaşık veri yapıları için kullanılır
list_example <- list(1, "a", TRUE)


# Faktör (factor): 
# Kategorik (sınıflandırılmış) verileri tutar, gruplama ve istatistiksel analizlerde kullanılır
factor_example <- factor(c("A", "B", "A"))

print(number)
print(text)
print(logical_value)
print(vector)
print(list_example)
print(factor_example)
