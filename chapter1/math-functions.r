# Temel Sayısal Fonksiyonlar Örnekleri

# Sayısal değerlerden oluşan bir vektör tanımlanıyor
values <- c(-5, 0, 10, 15)

# Mutlak değer hesaplama
cat("Mutlak Değer:", abs(values), "\n")
# Ortalama hesaplama
cat("Ortalama:", mean(values), "\n")
# Medyan hesaplama
cat("Medyan:", median(values), "\n")
# Standart sapma hesaplama
cat("Standart Sapma:", sd(values), "\n")
# Toplam değer hesaplama
cat("Toplam:", sum(values), "\n")
# Minimum ve maksimum değerleri bulma
cat("Minimum:", min(values), "\n")
cat("Maksimum:", max(values), "\n")
